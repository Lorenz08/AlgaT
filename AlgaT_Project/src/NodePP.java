public class NodePP {

    private Integer index_of_node;
    private Integer value_of_node;

    NodePP(Integer index, Integer value){
        index_of_node = index;
        value_of_node = value;
    }

    public Integer getIndex_of_node() {
        return index_of_node;
    }

    public Integer getValue_of_node() {
        return value_of_node;
    }
}

